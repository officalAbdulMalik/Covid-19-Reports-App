import 'dart:convert';

List<CountryStateModel> countryStateModelFromJson(String str) =>
    List<CountryStateModel>.from(
        json.decode(str).map((x) => CountryStateModel.fromJson(x)));

String countryStateModelToJson(List<CountryStateModel> data) =>
    json.encode(List<dynamic>.from(data.map((x) => x.toJson())));

